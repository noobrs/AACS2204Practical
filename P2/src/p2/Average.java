public class Average {
    public double calculateAverage(int x, int y){
        return (x + y) / 2;
    }
    
    public double calculateAverage(int x, int y, int z){
        return (x + y + z) / 3;
    }
    
    public double calculateAverage(double x, double y){
        return (x + y) / 2;
    }
    
    public double calculateAverage(double x, double y, double z){
        return (x + y + z) / 3;
    }
}
